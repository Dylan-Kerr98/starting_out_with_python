# Earlier you saw that the + operator is used to add numbers, when this operator is used with two strings it performs
# string concatenation.

# This means that is appends one string to another

print("This is " + "one string.")

# This can be useful for breaking up a string literal so a lengthy call to the print function can span multiple lines

print("Enter the amount " +
      "sales for each day " +
      "press (Enter).")
