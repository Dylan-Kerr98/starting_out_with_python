# As already mentioned, Python has two different division operators

# The / operator performs floating-point division & the // operator performs integer division

# The / operator gives its result as a floating-point number & the // operator gives its result as a integer

a = 5/2    # Result = 2.5
print(a)

b = 5//2    # Result = 2
print(b)    # When the // operator's result is positive it is truncated, basically its fraction part is thrown away


c = -5 / 2    # Result = -2.5
print(c)

d = -5 // 2    # Result = - 3
print(d)    # When the // operator's result is positive it is truncated, basically its fraction part is thrown away
