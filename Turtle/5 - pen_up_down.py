# You can raises an lower the turtle pen, when the pen is raised the turtle will not draw an line and when it is
# lowered it will

import turtle

turtle.forward(50)
turtle.penup()    # Raise pen
turtle.forward(25)
turtle.pendown()    # Lower pen
turtle.forward(50)
turtle.penup()    # Raise pen
turtle.forward(25)
turtle.pendown()    # Lower pen
turtle.forward(50)
turtle.penup()    # Raise pen
turtle.forward(25)
turtle.pendown()    # Lower pen
turtle.forward(50)
turtle.penup()    # Raise pen
turtle.forward(25)
turtle.pendown()    # Lower pen
turtle.forward(50)

turtle.done()
