# You can use the turtle.circle(radius) command to draw a circle, the radius size is passed as an argument

import turtle
turtle.circle(200)

#  The turtle.dot() command will make the turtle draw a simple dot

turtle.dot()    # Draw dot
turtle.forward(50)
turtle.dot()    # Draw dot
turtle.forward(50)
turtle.dot()     # Draw dot
turtle.forward(50)
turtle.dot()     # Draw dot
turtle.forward(50)
turtle.dot()     # Draw dot
turtle.forward(50)

turtle.done()
