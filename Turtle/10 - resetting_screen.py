# There are three commands that you can use too reset the turtle's graphics window:
import turtle

turtle.reset()    # This command erases all drawings that currently appear in the graphics window, it also reset the
# pen colour to black and resets the turtle to its original position

turtle.clear()    # This command simply erases all drawings that currently appear in the graphics window

turtle.clearscreen()    # This command does the exact some as turtle.reset(), however, it also resets the graphic
# window background colour to white
