# An if-else statement will execute one block of statements if its condition is true, or another block if its condition
# it is false

# This is know as a dual alternative decision structure, which has two possible paths of execution


temperature = int(input("What is the outside temperature?: "))

if temperature <= 40:
    print("A little cold, isn't it?")
else:
    print("Great weather we're having")

# When you write an if-else statement follow these guidelines for indentation:

# Make sure the if clause and the else clause are aligned
# The if clause and the else clause are each followed by a block of statements. Make sure the statements in the blocks
