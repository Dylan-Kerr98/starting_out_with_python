import turtle
# When the turtle first appears, its default heading is 0 degrees (East)

turtle.forward(50)

# There are two commands that can be used to turn the direct of the turtle, angle degrees of turn is passed to
# these as an argument

turtle.left(90)    # Turns turtle left
turtle.forward(200)
turtle.right(45)    # Turns turtle right
turtle.forward(200)

turtle.done()
